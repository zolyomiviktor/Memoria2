var valueS = -1;
parseInt(valueS)

$(document).ready(function() {
  $("#S").click(function(){ 
    if (valueS >= 9) {
      valueS = 0;
    }  else {
      valueS++;
    }
    document.getElementById("S").innerHTML = valueS;
  });
});

